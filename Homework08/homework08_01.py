import math

# ----------------------------------------------------------------------------------------------------------------------
# CLASSES
# ----------------------------------------------------------------------------------------------------------------------

class Rectangle:
    """Rectangle class using width and height"""

    def __init__(self, initW, initH):
        self.width = initW
        self.height = initH

    def area(self):
        """Calculate and return the area"""
        return self.width * self.height

    def perimeter(self):
        """Create a new method for the perimeter"""
        return (2*self.width) + (2*self.height)

    def diagonal(self):
        """Create a new method for the perimeter"""
        return math.sqrt((self.width ** 2) + (self.height ** 2))


# ----------------------------------------------------------------------------------------------------------------------
# FUNCTIONS
# ----------------------------------------------------------------------------------------------------------------------


# ----------------------------------------------------------------------------------------------------------------------
# VARIABLES
# ----------------------------------------------------------------------------------------------------------------------

a = Rectangle(4, 5)

# ----------------------------------------------------------------------------------------------------------------------
# BEGIN PROGRAM
# ----------------------------------------------------------------------------------------------------------------------

print("The area of your rectangle is:", a.area())
print("The perimeter of your rectangle is:", a.perimeter())
print("The diagonal length of your rectangle is:", a.diagonal())
